#include<stdio.h>
char a[10][7]=
{
	{037,021,021,021,021,021,037},//0
	{020,020,020,020,020,020,020},//1
	{037,020,020,037,001,001,037},//2
	{037,020,020,037,020,020,037},//3
	{021,021,021,037,020,020,020},//4
	{037,001,001,037,020,020,037},//5
	{037,001,001,037,021,021,037},//6
	{037,020,020,020,020,020,020},//7
	{037,021,021,037,021,021,037},//8
	{037,021,021,037,020,020,037},//9
};
int main()
{
	int i,j,k,m,x;
	int n[256];
	int t,z;
	int c[7];
	scanf("%d",&z);
	for(t=0; t<z; t++)
	{
		scanf("%d",&n[t]);
	}	
	for(t=0; t<z; t++)
	{	
		printf("%d:\n",n[t]);
		for(k=0; n[t]&&k<7; k++)
		{
			c[k]=n[t]%10;
			n[t]/=10;
		}
		for(i=0; i<7; i++)
		{
			for(m=k-1; m>=0; m--)
			{
				x=a[c[m]][i];
				for(j=0; j<7; j++)
				{
					if(x%2)
						printf("*");
					else
						printf(" ");
					x=x/2;
				}
			}
			printf("\n");
		}
	}
	return 0;
}

